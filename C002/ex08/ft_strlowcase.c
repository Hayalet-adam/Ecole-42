char	*ft_strlowcase(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		if ((str[c] <= 'Z') && (str[c] >= 'A'))
			str[c] += 32;
		c++;
	}
	return (str);
}
